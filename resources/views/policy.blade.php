                            our site. We use both session ID cookies and persistent cookies. For the session ID cookie,
                            once users close the browser, the cookie simply terminates. A persistent cookie is a small
                            text file stored on the user’s hard drive for an extended period of time. Persistent cookies
                            can be removed by following Internet browser help file directions.
                        </p>
